<h2>Duftnoten? Was sie sind und welche es gibt?</h2>

<p>Stöbern Sie durch den Onlineshop der ParfumGroup, so ist auf den Produktdetailseiten für Parfüms immer von Kopfnoten, Herznoten und Basisnoten die Rede. Prinzipiell setzen sich alle Parfüms meistens aus eben diesen Kopfnoten zusammen.</p>

<h3>Kopfnote</h3>

<p>Die Kopfnote spielt bei der Kaufentscheidung mit die wichtigste Rolle. Der Grund: Sie ist in den Minuten nach dem Auftragen die bestimmende Duftnote und bestimmt so den kaufentscheidenden ersten Eindruck. Die belebende Wirkung von Kopfnoten resultiert meistens aus spritzigen Nuancen. Üblich sind Zitrusfrüchte, Zimt, Minze, Gräser oder Bergamotte.</p>

<h3>Herznote</h3>

<p>Was nach Verflüchtigung der Kopfnote noch an duftenden Eindrücken in den Stunden danach bestehen bleibt, nennt sich Herznote. Diese markiert im Grunde den eigentlichen Charakter des jeweiligen Duftes. Inhaltlich wird die Herznote durch Blütennuancen wie etwa Iris, Rose und Jasmin, daneben aber auch Honig oder Beeren bestimmt.&nbsp;</p>

<h3>Basisnote</h3>

<p>Schließlich findet sich am Ende der zeitlichen Duftskala die Basisnote. Sie bildet im Grunde das Duft-Fundament eines jeden Parfums und rundet diesen ab. Bemerkbar macht sich die Basisnote üblicherweise erst nach wenigen Stunden. Bestandteile sind hier Moschus, Vanille, Weihrauch oder Hölzer. Ihr zeitlicher Wirkhorizont kann bis zu 24 Stunden betragen. &nbsp;</p>

